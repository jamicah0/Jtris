public enum BlockState {
    EMPTY,
    FILLED_SELECTED,
    FILLED_LOCKED
}

/*
    * This enum represents the different states a block can be in.
        * EMPTY: The block is empty and not selected.
        * EMPTY_SELECTED: The block is empty and selected.
        * EMPTY_OUTSIDE: The block is empty and outside the game board.
        * FILLED_SELECTED: The block is filled and selected.
        * FILLED_LOCKED: The block is filled and locked in place.
 */
